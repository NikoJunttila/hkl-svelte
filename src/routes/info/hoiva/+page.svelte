<h1>Hoiva-apu</h1>
<h2>Ikäihmisten hoiva-apu ja hieronta </h2>
<br>
<p>Tarvitsetko tai tarvitseeko läheisesi tukea arkisissa toimissa tai vaikkapa ulkoilu-seuraa, tai ohjattua toimintaa? Tai niska-hartia seudun hierontaa?</p>
<p>Täältä löytyy kokenut ja luotettava apu ikäihmisille.</p>
<p>Minulle erityisen tärkeää on, että asiakas tulee huomioiduksi ja saa edelleen tarvitsemaansa ylläpitävää toimintaa ja hyvinvointia elämäänsä.</p>
<p>Hoiva-apu palvelu sisältää henkilökohtaisen avustajan palveluja ja hieronta palvelua joka on tarkoitettu ikäihmisille asiakkaan kotiin, palvelu - tai hoitokotiin Sastamalan keskustan alueella ja läh.ympäristössä.</p>
<br>
<h3>Hoiva-avun henkilökohtaisen avustajan palveluihin sisältyy:</h3>
<br>
<h2>VIRIKE - JA HYVINVOINTI TOIMINTAA</h2>
<br>
<p>Asiakkaan kotona, palvelutalossa tai hoitokodissa. </p>
<p>Virike - ja hyvinvointi toiminta voi olla:</p>
<p>* Ohjattua toimintaa, jokin oma harrastus, ulkoilua, askartelua, tms. </p>
<p>* Voin toimia apuna esimerkiksi lääkärikäynnillä, käydä yhdessä kaupassa tai esimerkiksi kahvilla.</p>
<p>* Kodin kaivatessa kevyttä siistimistä, pölyjen pyyhkiminen, pyykinpesu ja imurointikin onnistuu asiakkaan omilla välineillä.</p>
<p>* Laitan mielelläni asiakkaan lempiruokaa tai leivon ja vietän hänen kanssaan aikaa yhdessä sovittuina ajankohtina.</p>
<p>* Läsnäoloa asiakkaalle.</p>
<p>* Hierontapalvelua </p>
